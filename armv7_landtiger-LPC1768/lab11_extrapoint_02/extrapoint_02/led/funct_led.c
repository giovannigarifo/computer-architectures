#include "lpc17xx.h"
#include "led.h"

#define LED_NUM	8 /* Number of user LEDs */
const unsigned long led_mask[] = { 1UL<<0, 1UL<<1, 1UL<<2, 1UL<< 3, 1UL<< 4, 1UL<< 5, 1UL<< 6, 1UL<< 7 };

/*----------------------------------------------------------------------------
  STATUS OF THE LEDS
 *----------------------------------------------------------------------------*/
extern unsigned char LED_STATUS;


/*----------------------------------------------------------------------------
  Function that turns on requested LED
 *----------------------------------------------------------------------------*/
void LED_On(unsigned int num) {
 
  LPC_GPIO2->FIOPIN |= led_mask[num];
}

/*----------------------------------------------------------------------------
  Function that turns off requested LED
 *----------------------------------------------------------------------------*/
void LED_Off(unsigned int num) {

  LPC_GPIO2->FIOPIN &= ~led_mask[num];
}

/*----------------------------------------------------------------------------
  Function that outputs value to LEDs
 *----------------------------------------------------------------------------*/
void LED_Out(unsigned int value) {
  int i;

  for (i = 0; i < LED_NUM; i++) {
    if (value & (1<<i)) {
      LED_On (i);
    } else {
      LED_Off(i);
    }
  }
}

/*----------------------------------------------------------------------------
	Car Red, Pedestrians Green => LD6 and LD5 on => P2.5 and P2.6 to 1
 *----------------------------------------------------------------------------*/
void LED_CarRed_PedGreen() {
	LED_Out(0x00000006);
}

/*----------------------------------------------------------------------------
	Car Red, Pedestrians Off => LD6 on => P2.5 to 1
 *----------------------------------------------------------------------------*/
void LED_CarRed_PedOff(){
	LED_Out(0x00000004);	
}

/*----------------------------------------------------------------------------
	Car Green, Pedestrians Red => LD8 and LD4 on => P2.3 and P2.7 to 1
 *----------------------------------------------------------------------------*/
void LED_CarGreen_PedRed(void){
	LED_Out(0x00000011);
}

/*----------------------------------------------------------------------------
	Car Yellow, Pedestrians Red => LD7 and LD4 on => P2.4 and P2.7 to 1
 *----------------------------------------------------------------------------*/
void LED_CarYellow_PedRed(void){
	LED_Out(0x00000009);
}

void LED_CarOff_PedOff(void){
	LED_Out(0x00000000);
}


